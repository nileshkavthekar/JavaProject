package com.canvas.demo;

public class Canvas {
	
	int Height;
	int Width;	
	
	public Canvas(int point1, int point2) {
		super();
		Width = point1;
		Height = point2;
		
	}
	public int getHeight() {
		return Height;
	}
	public void setHeight(int height) {
		Height = height;
	}
	public int getWidth() {
		return Width;
	}
	public void setWidth(int width) {
		Width = width;
	}
	
	
	

}
